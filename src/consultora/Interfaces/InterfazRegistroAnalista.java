    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        String nombre = nombreField.getText();
        String apellido = apellidoField.getText();
        String legajo = legajoField.getText();
        Object item = categoriasComboBox.getSelectedItem();
        String categoria = item.toString();

        try {
            Analista.registrarAnalista(nombre, apellido, legajo, categoria);
        } catch (IOException ex) {
            Logger.getLogger(InterfazRegistroAnalista.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        dispose();
        
    }//GEN-LAST:event_guardarActionPerformed

